# Menggabungkan String dengan join
# Menggabungkan dengan fitur Join
# Full String
a = "".join(["Haloo","Fauzan","Babi Kamu"])
print(a)
# Dengan Number
b = "134".join(["Angkaa","Angka"])
print(b)




# Memisahkan dengan fitur Split
# String yang dipisahkan oleh split selalu akan menjadi bentuk list 
c = "haloo fauzann".split()
print(c)